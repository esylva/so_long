#include "libft.h"

int	ft_atoi(const char *str)
{
	int	m;
	int	n;

	n = 0;
	m = 0;
	while (((*str >= 9) && (*str <= 13)) || *str == 32)
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
		{
			m++;
		}
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		n = n * 10 + (*str - 48);
		str++;
	}
	if (m % 2 == 1)
		n = -n;
	return (n);
}
